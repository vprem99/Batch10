package day1;

public class Swapping2 {
	public static void main(String[] args) {

		int a, b;

		a = 12;
		b = 25;

		a = a+b; // a=37
		b = a-b; // 37-25 = 12   : B=12
		a = a-b; // 37-12 = 25

		System.out.println(a);
		System.out.println(b);
		
	//	a==b && b==a
		//premchand.vishwakarma@aqmtechnologies.com
	}
}
